<?php
$a = 10;
$b = 5;

// Addition
$sum = $a + $b;  // Result: 15

// Subtraction
$difference = $a - $b;  // Result: 5

// Multiplication
$product = $a * $b;  // Result: 50

// Division
$quotient = $a / $b;  // Result: 2

// Modulus (remainder)
$remainder = $a % $b;  // Result: 0

// Exponentiation
$power = $a ** $b;  // Result: 100000

echo "Arithmetic Operations:\n";
echo "Sum: $sum\n";
echo "Difference: $difference\n";
echo "Product: $product\n";
echo "Quotient: $quotient\n";
echo "Remainder: $remainder\n";
echo "Power: $power\n";
?>
